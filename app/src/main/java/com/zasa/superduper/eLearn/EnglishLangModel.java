package com.zasa.superduper.eLearn;

public class EnglishLangModel {

    String title,Img;


    public EnglishLangModel() {
    }

    public EnglishLangModel(String title, String img) {
        this.title = title;
        Img = img;
    }

    public String getTitle() {
        return title;
    }

    public String getImg() {
        return Img;
    }
}
